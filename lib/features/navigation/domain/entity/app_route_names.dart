/// List of all route names used in temp feature.
///
/// Have to been filled with the entries like this:
/// ```dart
/// /// Name description.
/// static const String screenName = 'screenName';
/// ```
abstract class AppRouteNames {
  /// TempScreen route name.
  static const String tempScreen = 'tempScreen';

  /// Images screen name.
  static const String imagesScreen = 'imagesScreen';

  /// Image item info name.
  static const String imageItemScreen = '/imageItemScreen';
}
